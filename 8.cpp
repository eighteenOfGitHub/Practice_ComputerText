//编写一个程序解决爱因斯坦台阶问题：
//有人走以台阶，若以每步走2级则最后剩1级；
//若每步走3级则最后剩2级；
//若以每步走4级则最后剩3级；
//若以每步走5级则最后剩4级；
//若以每步走6级则最后剩5级；
//若以每步走7级则最后刚好不剩。
//问台阶共有几级？

#include<iostream>
using namespace std;

int main()
{
	int num = 0;

	while (num % 2 != 1 &&
		num % 3 != 2 &&
		num % 4 != 3 &&
		num % 5 != 4 &&
		num % 6 != 5)
	{
		num = num + 7;
	}
	cout << "台阶共有 " << num << " 级。" << endl;

	return 0;
}